package utils.campus;

public class TimeUtils {

	public static final int SECSPERMINUTE = 60;
	public static final int MINUTESPERHOUR = 60;
	public static final int HOURSPERDAY = 24;
	public static final int SECSPERHOUR = SECSPERMINUTE * MINUTESPERHOUR;
	public static final int SECSPERDAY = SECSPERHOUR * HOURSPERDAY;
	public static final int DAYSPERMONTH = 31;
	public static final int DAYSPERYEAR = 365;
	public static final int SECSPERYEAR = SECSPERDAY * DAYSPERYEAR;
	public static final int SECSPERMONTH = SECSPERDAY * DAYSPERMONTH;
	

}
